// il existe plusieurs techno pour creer des applications web
// voici les principales techno utilisees avec react

// -------------------------------------------------------------

// Web app Client : Vite , Create React App (CRA) , Parcel
// application qui s'execute dans le navigateur
// utilise HTML, CSS, JavaScript
// React est une librairie JavaScript pour construire des interfaces utilisateur

// Integration dans une page Web existante : CDN React
// on peut integrer react dans une page web existante
// en incluant les scripts react et react-dom
// puis en utilisant ReactDOM.render() pour rendre un composant react dans un element du DOM

// Web app Serveur : Next.js, Remix , Gasby
// application qui s'execute sur un serveur
// utilise Node.js, Express.js
// React peut etre utilise avec des frameworks comme Next.js pour le rendu cote serveur (SSR)

// Mobile app : React Native , Expo , Ionic
// application qui s'execute sur un appareil mobile
// utilise React Native pour construire des applications mobiles natives
// React Native permet d'utiliser les memes concepts que React pour creer des interfaces utilisateur mobiles

// Desktop app : Electron
// application qui s'execute sur un ordinateur de bureau
// utilise Electron.js pour construire des applications de bureau multiplateformes
// React peut etre utilise avec Electron pour creer des interfaces utilisateur de bureau

// -------------------------------------------------------------

